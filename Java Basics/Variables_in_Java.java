public class Variables_in_Java {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        String name = "Tony Stark";
        System.out.println(a);
        System.out.println(b);
        System.out.println(name);
        a = 50; // variable value changed
        System.out.println(a);
    }
}
// literals --> values which is contants
// variables --> values can be change
// identifiers --> name of variables, class name, filename, function name, etc.